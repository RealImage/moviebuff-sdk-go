package moviebuff

import v2 "github.com/RealImage/moviebuff-sdk-go/v2"

// MappedCPL contains information about a mapped CPL (Composition Playlist) from the Moviebuff API.
type MappedCPL = v2.MappedCPL

// MappedCPLMovie contains information about the movie associated with a mapped CPL.
type MappedCPLMovie = v2.MappedCPLMovie

// MappedCPLPart contains information about the movie part associated with a mapped CPL.
type MappedCPLPart = v2.MappedCPLPart
